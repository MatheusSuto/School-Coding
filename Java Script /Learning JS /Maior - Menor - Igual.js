let num_1 = parseInt(prompt("Digite o 1° número: "))
let num_2 = parseInt(prompt("Digite o 2° número: "))
if (num_1 > num_2){
    console.log("O 1° número é maior que o 2°")
} else if (num_2 > num_1) {
    console.log("O 2° número é maior que o 1°")
} else {
    console.log("Os números são iguais")
}
